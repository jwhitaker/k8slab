#!/usr/bin/env bash

# Install ansible
pipx install ansible-core

sudo apt update
sudo apt install -y iputils-ping apt-transport-https ca-certificates curl gnupg

# Install kubectl
curl -LO "https://dl.k8s.io/release/$(curl -L -s https://dl.k8s.io/release/stable.txt)/bin/linux/amd64/kubectl"   
chmod +x ./kubectl
sudo mv ./kubectl /usr/local/bin/kubectl   

# 1Password CLI
curl -sS https://downloads.1password.com/linux/keys/1password.asc | \
  sudo gpg --dearmor --output /usr/share/keyrings/1password-archive-keyring.gpg && \
  echo "deb [arch=$(dpkg --print-architecture) signed-by=/usr/share/keyrings/1password-archive-keyring.gpg] https://downloads.1password.com/linux/debian/$(dpkg --print-architecture) stable main" | \
  sudo tee /etc/apt/sources.list.d/1password.list && \
  sudo mkdir -p /etc/debsig/policies/AC2D62742012EA22/ && \
  curl -sS https://downloads.1password.com/linux/debian/debsig/1password.pol | \
  sudo tee /etc/debsig/policies/AC2D62742012EA22/1password.pol && \
  sudo mkdir -p /usr/share/debsig/keyrings/AC2D62742012EA22 && \
  curl -sS https://downloads.1password.com/linux/keys/1password.asc | \
  sudo gpg --dearmor --output /usr/share/debsig/keyrings/AC2D62742012EA22/debsig.gpg && \
  sudo apt update && sudo apt install 1password-cli

# Add alias for hl.sh script
echo 'alias hl="/workspaces/k8slab/scripts/hl.sh"' >> ~/.zshrc
echo 'alias k="kubectl"' >> ~/.zshrc
echo 'export KUBECONFIG=/workspaces/k8slab/.kubeconfig' >> ~/.zshrc

make dependencies